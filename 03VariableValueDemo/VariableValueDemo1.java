public class VariableValueDemo1{
    public static void main(String[] args){
        //byte
        byte b = 10;
        System.out.println(b);

        //short
        short s = 20;
        System.out.println(s);

        //int
        int i = 30;
        System.out.println(i);

        //long
        /*
         *如果要定义long类型的变量
         *在数字后面需要加一个L作为后缀，大小写均可
         *建议使用大写
         */
        long n = 9999999999L;
        System.out.println(n);

        //float
        //定义float类型变量的时候需要加F作为后缀
        float f = 10.1F;
        System.out.println(f);

        //double
        double d = 88.8;
        System.out.println(d);

        //char
        char c = '好';
        System.out.println(c);

        //boolean
        boolean o = true;
        System.out.println(o);
    }
}
