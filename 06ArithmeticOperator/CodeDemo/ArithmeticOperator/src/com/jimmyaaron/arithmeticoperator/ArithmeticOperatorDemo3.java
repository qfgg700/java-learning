package com.jimmyaaron.arithmeticoperator;

public class ArithmeticOperatorDemo3 {
    public static void main(String[] args) {
        int a = 10;

        a++;
        System.out.println(a);

        ++a;
        System.out.println(a);

        a--;
        System.out.println(a);

        --a;
        System.out.println(a);
    }
}
